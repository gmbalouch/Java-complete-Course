
public class DataTypesExamples {

	public static void main(String[] args) {
		byte a = 127;       
		int num=4326;
		
		double d=15.56;
		float f=12.43f;
		
		char c='a';
		
		System.out.println("The value of a variable "+a);
		System.out.println("The value of num variable "+num);
		System.out.println("The value of d variable "+d);
		System.out.println("The value of f variable "+f);
		System.out.println("The value of c variable "+c);
	}

}
